package lecture3;
import java.util.Scanner;
public class pattern2 {
	public static void main (System args[]) {
	Scanner s = new Scanner(System.in);
	System.out.println("Enter the no = ");
	int n = s.nextInt();
	int i = 1 ;
	while(i<=n) {
		int j = 1;
		while(j<= n ) {
		j=j+1 ;
	System.out.print(i);
		}
	}
		System.out.println();
		i = i+1 ;
	}
	
	
}
