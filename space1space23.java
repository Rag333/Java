package lecture3;

public class space1space23 {
	public static void main( String [] args) {
	int n = 4 ;
	int i = 1 ;
	int val = 1 ;
	while(  i <= n ) {
		int j = 1 ;
		while( j <= i) {
			
			System.out.print(val );
			val++ ;
			j ++ ;
			
		}
		System.out.println();
		i ++ ;
		
	}

}
}